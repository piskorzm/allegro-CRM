class Address {
    constructor(address_ID,
                postcode,
                city,
                street,
                house_no,
                flat_no) {
        this.address_ID = address_ID;
        this.postcode = postcode;
        this.city = city;
        this.street = street;
        this.house_no = house_no;
        this.flat_no = flat_no;
    }
}