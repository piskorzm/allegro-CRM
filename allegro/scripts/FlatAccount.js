class FlatAccount {
    constructor(ID,
                NIP,
                first_name,
                surname,
                company_name,
                login,
                email,
                tel1,
                tel2,
                postcode,
                city,
                street,
                house_no,
                flat_no) {
        this.ID = ID;
        this.NIP = NIP;
        this.first_name = first_name;
        this.surname = surname;
        this.company_name = company_name;
        this.login = login;
        this.email = email;
        this.tel1 = tel1;
        this.tel2 = tel2;
        this.postcode = postcode;
        this.city = city;
        this.street = street;
        this.house_no = house_no;
        this.flat_no = flat_no;
    }
}